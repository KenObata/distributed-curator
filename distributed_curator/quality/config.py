PUNCTUATION_CHARS: frozenset[str] = frozenset(chr(c) for c in _PUNCTUATION_CODEPOINTS)

# Single string of all punctuation chars, for Spark's translate() which maps
# characters by Unicode codepoint (handles supplementary-plane chars). Used to
# detect symbol-only words: length(translate(word, PUNCTUATION_STRING, '')) == 0.
# Chosen over an rlike character class: 2.1x faster in benchmarks, no regex
# escaping concerns.
PUNCTUATION_STRING: str = "".join(chr(c) for c in _PUNCTUATION_CODEPOINTS)


@dataclass(frozen=True)
class HeuristicConfig:
    """Enable/disable flags and parameters for each heuristic rule group.

    Each ``enable_*`` flag controls one group of output columns (see
    heuristics.py for the column <-> group mapping). Disabled groups emit no
    columns. All groups are enabled by default.
    """

    enable_word_count: bool = True
    enable_mean_word_length: bool = True
    enable_symbol_ratios: bool = True
    enable_line_format_fractions: bool = True  # bullet-start / ellipsis-end line fractions
    enable_alpha_word_fraction: bool = True
    enable_stop_words: bool = True
    enable_dup_lines_paragraphs: bool = True

    stop_words: tuple[str, ...] = GOPHER_STOP_WORDS
    bullet_prefixes: tuple[str, ...] = ("\u2022", "-")  # placeholder, fixed below
    ellipsis_suffixes: tuple[str, ...] = ("...", "\u2026")  # placeholder, fixed below
